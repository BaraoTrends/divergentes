<li><strong>Medicação:</strong> Estimulantes ou não-estimulantes que ajudam a equilibrar a química cerebral.</li>
<li><strong>Mudanças no Estilo de Vida:</strong> Exercícios físicos regulares liberam dopamina de forma saudável.</li>
