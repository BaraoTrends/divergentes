<h2>Quais são os principais sintomas de TDAH em adultos e crianças?</h2>

<p>Os sinais variam drasticamente de pessoa para pessoa. Enquanto uma criança pode subir em móveis e não parar quieta, um adulto pode manifestar a hiperatividade como uma mente que nunca desliga, gerando ansiedade e insônia.</p>

<p>Geralmente, dividimos os sintomas em três pilares principais:</p>

<ul>
<li><strong>Desatenção:</strong> Dificuldade em manter o foco em tarefas longas, erros por descuido, perder objetos com frequência e parecer não ouvir quando chamam.</li>
<li><strong>Hiperatividade:</strong> Inquietação motora, falar excessivamente, dificuldade em realizar atividades silenciosas e sensação de estar "ligado na tomada".</li>
<li><strong>Impulsividade:</strong> Responder perguntas antes de serem concluídas, dificuldade em esperar a vez e interromper a fala alheia constantemente.</li>
</ul>

<p>É importante notar que nem todo mundo apresenta todos os sinais. Algumas pessoas são predominantemente desatentas, outras predominantemente hiperativas-impulsivas, e há o tipo combinado, que é o mais comum.</p>

