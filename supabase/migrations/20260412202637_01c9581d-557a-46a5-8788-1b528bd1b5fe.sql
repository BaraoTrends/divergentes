<h3>A diferença entre o TDAH tipo desatento e o tipo hiperativo</h3>

<p>O tipo desatento costuma ser o "sonhador". Aquela pessoa que está na sala, mas parece estar em outro planeta. Por não causarem "problemas" ou barulho, muitas meninas e mulheres acabam sendo diagnosticadas tardiamente.</p>

<p>Já o tipo hiperativo é mais visível. No entanto, em adultos, essa hiperatividade física costuma se transformar em uma agitação mental. É aquela necessidade constante de mudar de hobby, trocar de emprego ou a incapacidade de relaxar mesmo nas férias.</p>

<h2>Como é feito o diagnóstico de TDAH no Brasil?</h2>

<p>Não existe um exame de sangue ou de imagem que aponte: "Aqui está o TDAH". O diagnóstico é essencialmente clínico, baseado no histórico de vida do paciente, relatos de familiares e observação de padrões de comportamento.</p>

<p>No Brasil, o processo geralmente envolve uma equipe multidisciplinar. Médicos psiquiatras ou neurologistas lideram o processo, muitas vezes contando com a ajuda de neuropsicólogos para realizar testes de funções cognitivas.</p>

<p>Para um diagnóstico preciso, os sintomas precisam estar presentes desde a infância e causar prejuízos reais em pelo menos dois âmbitos da vida — como no trabalho e no relacionamento amoroso.</p>

