<h2>Afinal, o que é TDAH e como ele afeta o cérebro?</h2>

<p>Cientificamente falando, o TDAH é um transtorno do neurodesenvolvimento com fortes bases genéticas. Ele se manifesta principalmente pela dificuldade em regular a atenção, controlar impulsos e, em muitos casos, lidar com uma inquietação física ou mental constante.</p>

<p>Diferente do que muita gente pensa, o problema não é a "falta" de atenção. Na verdade, quem tem TDAH costuma ter atenção <em>demais</em> para tudo ao mesmo tempo. O filtro que deveria separar o que é importante do que é irrelevante simplesmente não funciona bem.</p>

<p>A neurociência explica que isso ocorre devido a uma desregulação de neurotransmissores, especialmente a dopamina e a noradrenalina, nas áreas frontais do cérebro. Essas regiões são responsáveis pelas funções executivas — o nosso "maestro" interno que planeja, organiza e executa tarefas.</p>

